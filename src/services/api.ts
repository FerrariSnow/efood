import { createApi, fetchBaseQuery } from '@reduxjs/toolkit/query/react'
import { ExtendedStoreType, StoreType } from '../pages/Home'

type Product = {
  id: number
  price: number
}

type PurchasePayload = {
  products: Product[]
  delivery: {
    receiver: string
    address: {
      description: string
      city: string
      zipCode: string
      number: number
      complement?: string
    }
  }
  payment: {
    card: {
      name: string
      cardNumber: string
      code: number
      expires: {
        month: number
        year: number
      }
    }
  }
}

const api = createApi({
  baseQuery: fetchBaseQuery({
    baseUrl: 'https://fake-api-tau.vercel.app/api/efood/'
  }),
  endpoints: (builder) => ({
    getStores: builder.query<ExtendedStoreType, void>({
      query: () => 'restaurantes'
    }),
    getStore: builder.query<StoreType, string>({
      query: (id) => `restaurantes/${id}`
    }),
    purchase: builder.mutation<any, PurchasePayload>({
      query: (body) => ({
        url: 'checkout',
        method: 'POST',
        body
      })
    })
  })
})

export const { useGetStoresQuery, useGetStoreQuery, usePurchaseMutation } = api

export default api
